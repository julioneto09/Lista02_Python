'''
Listas: 
Sequências mutáveis
Podem receber valores de qualquer tipo (string, int, float, boleano);
Cada item pode ser acessado pelo seu indice(que é numerico)
'''

'''
Tuplas:
Sequências imutaveis
Podem receber valores de qualquer tipo (string, int, float, boleano);
'''

'''
Conjuntos (set):
Coleção não ordenada de elementos
Os elementos são unicos
Mutável
'''

'''
Dicionarios
Uma coleção de indices(chaves) e valores que são associados
Os indices são literais
'''